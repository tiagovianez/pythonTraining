#Utilizando valores flutuantes
n = float(input('Digite um valor: '))
print(n)

#Utilizando valores booleanos: Se for digitado um valor, a resposta será 'True', caso contrário 'False'
n = bool(input('Digite um valor: '))
print(n)

#Testando se um valor é númérico ou não
n = str(input('Digite um valor: '))
print(n.isnumeric())

#Verificando de uma palavra dita está em letra maiúscula
n = str(input('Digite algo: '))
print(n.isupper())
print((n.i))